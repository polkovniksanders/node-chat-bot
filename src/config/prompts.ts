export function buildNewsDigestPrompt(history: HistoryEntry[], params: NewsParams): string {
  const historySection =
    history.length > 0
      ? `ЗАПРЕЩЁННЫЕ темы (уже использовались — строго не повторять):
${history.map((e) => `- ${e.date}: [${e.news.location}, ${e.news.time}, «${e.news.event}»]`).join('\n')}

`
      : '';

  const paramsSection = `ОБЯЗАТЕЛЬНЫЕ параметры для ЭТОГО выпуска (строго соблюдать, не менять):
место — ${params.location} | время — ${params.time} | тема события — ${params.event} | эмоция Стёпы — ${params.emotion}

`;

  return `${paramsSection}${historySection}${NEWS_DIGEST_BASE}`;
}

