import { generateContent } from '@/ai/generateContent.js';

const SYSTEM_PROMPT =
  'Ты — кот Стёпка из Челябинска. Генерируешь необычные клички для домашних животных. ' +
  'Отвечай строго по формату — только HTML-теги <b> и <i>, никаких других символов разметки. ' +
  'Только на русском языке.';

const USER_PROMPT =
  'Придумай ровно 5 необычных, оригинальных кличек для домашних животных (любых: коты, собаки, птицы, хомяки и т.д.). ' +
  'Для каждой клички дай краткое описание в 1–2 предложения: какому питомцу подойдёт и почему звучит интересно. ' +
  'В самом конце добавь короткий комментарий от лица кота Стёпки (1–2 предложения). ' +
  'Выводи строго в таком формате (без вводных фраз, сразу начинай с "1."):\n\n' +
  '1. 🌟 <b>Кличка</b> — описание\n' +
  '2. 🦊 <b>Кличка</b> — описание\n' +
  '3. 🐾 <b>Кличка</b> — описание\n' +
  '4. ⚡ <b>Кличка</b> — описание\n' +
  '5. 🎭 <b>Кличка</b> — описание\n\n' +
  '💬 <i>Стёпка:</i> «комментарий»';

export async function generatePetNamesPost(): Promise<string> {
  const aiContent = await generateContent(SYSTEM_PROMPT, USER_PROMPT);

  return `🐾 <b>5 необычных кличек для питомцев</b>

${aiContent}

#кличкидляживотных #питомцы #животные #степка
<a href="https://t.me/stepka_and_twitty">⭐ Подписаться</a>`;
}
